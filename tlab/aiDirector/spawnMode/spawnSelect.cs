function ADE::selectSpawnObject(%this,%obj) {
	devLog("Selecting Spawn Object:",%obj);
	%this.selectedSpawn = %obj;
	syncParamArray(%this.spawnArray,true);
}
//------------------------------------------------------------------------------

//==============================================================================
function ADE_SpawnObjTree::onSelect(%this,%objID) {
	ADE.selectSpawnObject(%objID);
}
